#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;
	int	i;

	j = 0;
	i = 1;
	while (i < ac)
	{
		while (av[i][j])
		{
			write(1, &av[i][j], 1);
			j++;
		}
		j = 0;
		write(1, "\n", 1);
		i++;
	}
}
