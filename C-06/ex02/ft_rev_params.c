#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;
	int	i;

	j = 0;
	i = ac - 1;
	while (i > 0)
	{
		j = 0;
		while (av[i][j])
		{
			write(1, &av[i][j], 1);
			j++;
		}
		i--;
		write(1, "\n", 1);
	}
}
